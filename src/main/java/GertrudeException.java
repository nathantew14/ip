/**
 * Represents an exception specific to the Gertrude chatbot.
 */
public class GertrudeException extends Exception {
    /**
     * Constructs a GertrudeException with the specified error message.
     *
     * @param message The error message.
     */
    public GertrudeException(String message) {
        super(message);
    }
}